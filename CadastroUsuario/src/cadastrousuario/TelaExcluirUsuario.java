    public void recebeUsuarioParaExcluir(Pessoa p){
       
       txtNomeExcluir.setText(p.getNome());
       txtNomeExcluir.setEnabled(false);
       
       txtSobrenomeExcluir.setText(p.getSobreNome());
       txtSobrenomeExcluir.setEnabled(false);
       
       txtDataNascimentoExcluir.setText(p.getDataNascimento().replace("-", "/"));
       txtDataNascimentoExcluir.setEnabled(false);
       
       txtSexoExcluir.setText(p.getSexo());
       txtSexoExcluir.setEnabled(false);
       
       txtEmailExcluir.setText(p.getEmail());
       txtEmailExcluir.setEnabled(false);
       
       txtTelefoneExcluir.setText(p.getTelefone()); 
       txtTelefoneExcluir.setEnabled(false);
       
       idPessoa = p.getId();
    }

