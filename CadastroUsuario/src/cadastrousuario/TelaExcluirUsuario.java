    private void controleTela (){
        if( objListarUsuarios == null){
            dispose();
            objListarUsuarios = new TelaListarUsuarios();
            objListarUsuarios.setVisible(true);
        }else{
            dispose();
            objListarUsuarios.setVisible(true);
            objListarUsuarios.setState(TelaExcluirUsuario.NORMAL);
        }
    }
